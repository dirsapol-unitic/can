<h3>REGISTRAR A LOS RESPONSABLES DEL CAN2020: </h3>
<br/>
<p class="c2 c22"><span class="c6 c1"><b>A CONTINUACION SE MOSTRARA LO SIGUIENTE:</b></span></p>
<p><img alt="" src='{{ asset ("/images/image74.jpg") }}' height="80%" width="80%" ></p>
<br/>
<p><b>Aqui se enumera el responsable de la IPRESS, responsable de FARMACIA y 5 responsables por los RUBROS que esta dividido el PETITORIO que ayudaran a realizar el CAN2020</b></p>
<br/>
<ul>
	<li>Un responsable puede aparecer en uno o mas rubros</li>
	<li>Si su IPRESS cuentan con especialista en cada rubro que se muestra en el listado de abajo, el responsable del registro podra pedir la colaboracion para participar en el CAN2020</li>
	<li>Solo hay un responsable del registro del CAN2020, que es la persona que ha sido asignado por la IPRESS, para el registro y consolidacion del CAN2020, dicha persona aparecera como registrador del llenado del CAN2020</li>
	<li>Finalizado el registro del CAN2020,tanto en medicamentos y dispositivos medicos, al final de cada rubro apareceran 4 nombres, que han participado en el llenado del RUBRO. Dichos documentos deberan ser firmados por los participantes.</li>
	<li>Si usted ha sido asignado como responsable de su IPRESS para el llenado del CAN2020, podra llenar todos los rubros con su nombre en caso no hubieran especialistas que puedan ayudar hacer el pedido del CAN2020</li>
</ul>
<br/>
<p><b>JEFE DE LA IPRESS:</b>Se registrara al jefe de la IPRESS </p>
<p><b>JEFE DE FARMACIA:</b>Se registrara al responsable o jefe que se encuentra a Cargo de la Farmacia </p>
<p><b>Responsable de Productos Farmaceuticos:</b> Se pueden considerar al especialista quimico farmaceutico que tenga conocimiento del tema, que bien podria ser el responsable de Farmacia u otro personal que maneje medicamentos en la farmacia.</p>
<p><b>Responsable de Material Biomedico, Instrumental Quirurgico, y Productos afines:</b>Si su IPRESS, maneja productos en este rubros, en colaboracion con el responsable de farmacia, un especialista o medico pueden hacer el requerimiento de dichos productos </p>
<p><b>Responsable de Material e Insumo Odontologico:</b>Si su IPRESS, maneja productos en este rubros, en colaboracion con el responsable de farmacia, un especialista o medico pueden hacer el requerimiento de dichos productos </p>
<p><b>Responsable de Material e Insumos de Laboratorio:</b>Si su IPRESS, maneja productos en este rubros, en colaboracion con el responsable de farmacia, un especialista o medico pueden hacer el requerimiento de dichos productos </p>
<p><b>Responsable de Material Fotografico o Fonotecnico:</b>Si su IPRESS, maneja productos en este rubros, en colaboracion con el responsable de farmacia, un especialista o medico pueden hacer el requerimiento de dichos productos </p>
<br/>
<p>En la parte de la derecha hay un icono de color verde, la cual podra hacer clic para el registro del responsable.</p>
<p><img alt="" src='{{ asset ("/images/image76.jpg") }}' height="30%" width="30%" ></p>
<br/>
<p>Una vez ingresado nos aparecera la siguiente ventana.</p>
<p><img alt="" src='{{ asset ("/images/image75.jpg") }}' height="80%" width="80%" ></p>
<br/>
<p><b>1.- DNI </b> Al ingresar el DNI del responsable, si esta en nuestra base de datos se llenaran automaticamente los demas campos, caso contrario se tendra que editar</p>
<p><b>2.- Nombre:</b> Es el nombre del responsable</p>
<p><b>3.- Apellido Paterno:</b> Es el apellido paterno del responsable</p>
<p><b>4.- Apellido Materno:</b> Es el apellido materno del responsable</p>
<p><b>5.- Grado: </b> Es el Grado del responsable</p>
<p><b>6.- Tipo de Responsable </b> Aqui aparecera una de las 3 opciones que se tiene, Jefe de IPRESS, Jefe o Responsable de Farmacia, Responsable Rubro</p>
<p><b>7.- Rubro </b> No se aplica, ni para Jefe de la IPRESS, ni para Jefe o Responsable de la Farmacia, solo para los rubros, que pueden ser Productos Farmaceuticos; Material Biomedico, Instrumental Quirurgico y Productos afines; Material e Insumo Odontologico; Material e Insumos de Laboratorio; Material Fotografico o Fonotecnico</p>
<p><b>8.- Boton Guardar </b> Clic para registrar al responsable </p>
<p><b>9.- Boton Cancelar </b> Clic para salir sin registrar al responsable</p>
<br/>
<p>Si se desea modificar a un responsable, se podra realizar haciendo clic en el boton azul de Editar</p>
<p><img alt="" src='{{ asset ("/images/image77.jpg") }}' height="80%" width="80%" ></p>
<p>Clic en Editar; y nos mostrara la ventana para hacer las modificaciones que se desee, y a continuacion guardamos el registro.</p>
