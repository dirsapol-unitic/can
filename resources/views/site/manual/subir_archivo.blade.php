<p class="c2 c19"><span class="c1 c18"><strong>SUBIR ARCHIVO</strong></span></p>
<ul><li>Esta opcion aparecera cuando se haya cerrado el CAN2020, tanto de medicamentos como de dispositivos medicos</li>
<li>Una vez que haya terminado el CAN2020, se tendra que remitir los archivos a la UGPFDMPS, con las firmas y sellos (los que dispongan) de los encargados de los RUBROS, mas el JEFE de la IPRESS, y el RESPONSABLE o JEFE DE FARMACIA</li>
<li>Estos archivos deberan ser subidos a la plataforma para que la UGPFDMPS, los pueda visualizar, estos archivos ya no sera necesario ser enviado por correo electronico </li>
